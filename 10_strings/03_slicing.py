name = "Tanmay123456789"
print(name[0:2])  # This is like a range from 0 to 2-1 with character on 2 number will not be included as result: Ta
print(name[4:-1])  # This is like a range from 0 to 5 
# print(name[0:10:n])  # This will skip n-1 characters after first ch and then cycle will continue
print(name[0:10:1])
print(name[0:10:2])  # This will skip 1 character
print(name[0:10:3])  # This will skip 2 character
print(name[0:16:3])
print(name[:4])  #  will print from start to 4-1 char place that is m
print(name[3:])  # will print from char place 3 till the end char place 15 3:15 will print the whole length